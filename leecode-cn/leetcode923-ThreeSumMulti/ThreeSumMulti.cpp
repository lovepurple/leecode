int Solution::threeSumMulti(vector<int>& A, int target)
{
	if (A.size() == 3)
		return  A[0] + A[1] + A[2] == target;

	vector<int> vecElement;
	int times = 0;
	dfs(A, 0, target, vecElement, times);

	return times;
}

void Solution::dfs(vector<int> A, int index, int target, vector<int>& vecCurrent, int& times)
{
	if (target < 0)
		return;

	if (target == 0 && vecCurrent.size() == 3)
	{
		if (vecCurrent.size() == 3)
			times = (times + 1) % 1000000007;

		return;
	}

	if (index >= A.size())
		return;

	for (int i = index; i < A.size(); ++i)
	{
		int element = A[i];
		vecCurrent.push_back(element);
		dfs(A, i + 1, target - element, vecCurrent, times);
		vecCurrent.pop_back();
	}
}
